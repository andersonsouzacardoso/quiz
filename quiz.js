let aplicativo = {
 start: function(){
    this.perguntaAtual = 0;
    this.totalPontos = 0;

    let minhasAlternativas = document.querySelectorAll('.alternativa');
    minhasAlternativas.forEach((element,index)=>{
        element.addEventListener('click', ()=>{
           this.checaResposta(index);
        })
    })

    this.atualizaPontos();
    aplicativo. mostraquestao(pergunta[this.perguntaAtual]);
},
mostraquestao: function(q){
    this.questaoAtual = q;
    //Mostrando perguntas
    let titleDiv = document.getElementById('perguntas');
    titleDiv.textContent = q.perguntas;
    //Mostrando Alternativas
    let minhasAlternativas = document.querySelectorAll('.alternativa');
    minhasAlternativas.forEach(function(element,index){
        element.textContent = q.alternativas[index];
        
    })
},

    proximaPergunta: function(){
        this.perguntaAtual++;
        if(this.perguntaAtual == pergunta.length){
            this.perguntaAtual = 0;
        }
    },

    checaResposta: function(user){

        if(this.questaoAtual.correta == user){
            console.log('ta certo')
            this.totalPontos++;
            this.mostraResposta(true);
            document.getElementById('resultado').style.backgroundColor = 'green';
        }else{
            console.log('ta não')
            this.mostraResposta(false);
            document.getElementById('resultado').style.backgroundColor = 'red';
        }
        this.atualizaPontos();
        this.proximaPergunta();
        this.mostraquestao(pergunta[this.perguntaAtual])
    },

    atualizaPontos: function(){
        let scoreDiv = document.getElementById('pontos')
        scoreDiv.textContent = `Sua pontuação é: ${this.totalPontos}`
        
    },

    mostraResposta: function(correta){
        let resultDiv = document.getElementById('resultado')
        let result = ''
        //Formatar como a mensagem será exibida
        if(correta){
            result = 'Resposta Correta'
        }else{
            //Obtendo a questão atual
            let perguntas = pergunta[this.perguntaAtual]
            //Obtenha o indice da resposta correta da questão atual
            let indiceRespostacorreta = perguntas.correta;
            //Obter o texto correto da questão atual
            let textoCorreto = perguntas.alternativas[indiceRespostacorreta]
            result = `Incorreto! Resposta Correta: ${textoCorreto}`
        }

        resultDiv.textContent = result
    }

}
aplicativo. start();